float RSSI_get_coef_n(uint8_t RSSI_d0, uint8_t RSSI, double d, double d0)
{
	float n;
	n = ((RSSI_d0 - RSSI) / (10 * log10(d / d0)));
	return n;
}
//-------------------------------------------------------------------//
float RSSI_get_Dist(uint8_t RSSI_d0, uint8_t RSSI, double d0, float n)
{
	float Dist;
	Dist = d0 * pow(10, (RSSI_d0 - RSSI) / (10 * n));
	return Dist;
}
